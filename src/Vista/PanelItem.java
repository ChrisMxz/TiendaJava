    private void jscantidadStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jscantidadStateChanged
        double total=0.0;
        int stock,seleccionado,antes=0;
        seleccionado=Integer.parseInt(jscantidad.getValue().toString());//el obtenido por el selector
        stock=producto.getStock();//el stock aun disponible del producto
            
        if(bandera){//cuando se esta editando
        antes=item.getCantidad();
        //stock=stock-(seleccionado-antes)
        stock=stock-(seleccionado-antes);
        }
        total=seleccionado*producto.getPrecio(); //calculando el monto
        lbstock.setText("Disponible: "+stock);
        txttotal.setText(String.valueOf(total));
    }//GEN-LAST:event_jscantidadStateChanged

    private void btnaceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnaceptarMouseClicked
        // Aceptar
        int num = Integer.parseInt(jscantidad.getValue().toString());//obteniendo el numero de productos seleccionados
        pedidos=new CRUDPedidos();

        //verificando una cantidad dentro del rango
        if (num > 0) {
            if (!bandera) {//se esta insertando 
                insertar(num);
            } else {//aqui se actualiza
                actualizar(num);
            }
            pedido.setCantidadproductos(items.cuentaproductos(pedido.getId_pedido()));//actualizando num productos
            pedido.setTotal(items.sumamonto(pedido.getId_pedido()));    //actualizando total
            //actualizando en bd
            pedidos.actualiza(pedido);

            panel.estado();//cargamos el estado
            this.dispose();//salimos
        } else {
            JOptionPane.showMessageDialog(this, "Elija una cantidad correcta");
        }
    }//GEN-LAST:event_btnaceptarMouseClicked

    private void btneliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btneliminarMouseClicked
        // se presiona eliminar
        if (JOptionPane.showConfirmDialog(this, "Se eliminará ¿Desea continuar?",
                "Aviso", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            eliminar();
            panel.estado();
            this.dispose();
        }


    }//GEN-LAST:event_btneliminarMouseClicked

