    public void estado(){
        txtnombre.setText(producto.getNombre());
        txtconcepto.setText(producto.getConcepto()+"- "+producto.getDescripcion());
        txtprecio.setText("$ "+String.valueOf(producto.getPrecio()));
        lbstock.setText("Disponible: "+String.valueOf(producto.getStock()));
        
        btneliminar.setVisible(bandera);//mostramos eliminar
        
        SpinnerNumberModel modelo = new SpinnerNumberModel();
        modelo.setMinimum(0);//valor minimo a seleccionar
        modelo.setMaximum(producto.getStock()); //colocando el valor maximo cuando no estamos editando
        jscantidad.setModel(modelo);//cargando el modelo dentro del seleccionador
       

        if(bandera){//se esta editando
            modelo.setMaximum(producto.getStock()+item.getCantidad()); //colcando el valor maximo cuando estamos editando 
            jscantidad.setValue(item.getCantidad());//seleccionamos el total de items que tiene
        }   
    }
    
    
    public void insertar(int num){
        items=new CRUDItems();
        producto.setStock(producto.getStock() - num); //actualizando el stock
        productos.actualizar(producto); //actualizando en bd
        item=new Item();
        item.setId_pedido(pedido.getId_pedido());
        item.setId_producto(producto.getId_producto());
        item.setCantidad(num);
        item.setMonto(Double.parseDouble(txttotal.getText()));
        items.insertar(item);//Insertando item en bd
    }
    
    public void actualizar(int num){
        productos=new CRUDProductos();
        items=new CRUDItems();
        //se esta editando el item
        //stock=stock-(nuevo-antes)
        producto.setStock(producto.getStock()-(num-item.getCantidad()));//se actualizo el stock
        item.setCantidad(num);//actualizamos la cantidad de datos
        item.setMonto(Double.parseDouble(txttotal.getText()));//actualizando el monto
        
        //Guardamos en la bd
        productos.actualizar(producto);
        items.actualizar(item);
    }
    
    public void eliminar(){
        productos=new CRUDProductos();
        items=new CRUDItems();
        pedidos=new CRUDPedidos();
        //stock=stock+numproductos
        items.eliminar(item.getId_item());//eliminamos el item
        producto.setStock(producto.getStock()+item.getCantidad()); //recuperamos la el stock
        pedido.setCantidadproductos(items.cuentaproductos(pedido.getId_pedido()));//actualizando num productos
        pedido.setTotal(items.sumamonto(pedido.getId_pedido()));    //actualizando total
        
        //actualizando en bd
        pedidos.actualiza(pedido);
        productos.actualizar(producto);
        
    }
    
    

    
    


