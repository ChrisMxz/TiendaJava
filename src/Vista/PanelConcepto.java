    private void Limpiar() {
        //limpiando los datos del formulario
        txtid.setText("");
        txtconcepto.setText("");
    }
    
    private void estado(){
        cargaTabla();
        btnelimina.setVisible(edicion); //mostrar boton eliminar 
        btnbusca.setVisible(!edicion);
        btseleccionar.setVisible(edicion);
        txtbusca.setVisible(!edicion);
        lb1.setVisible(!edicion);
        if(!edicion){//No se esta editando
            btnacepta.setText("Agregar");
        }else{//se esta editando
            btnacepta.setText("Guardar");
        }   
    }
    
    private void cargaConcepto() {
        try {
            int fila = tb.getSelectedRow(); //Obtenemos la fila

            //Creando objeto para utilizar despues en actualizar, eliminar, seleccionar
            concepto.setId_concepto(Integer.parseInt(tb.getValueAt(fila, 0).toString()));
            concepto.setConcepto(tb.getValueAt(fila, 1).toString());

            //Cargando los datos en el formulario
            txtid.setText(String.valueOf(concepto.getId_concepto()));
            txtconcepto.setText(concepto.getConcepto());

            //se seleciona un objeto y habilitamos su edicion
            edicion = true;
            estado();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error al seleccionar de tabla: " + e);
        }
    }

