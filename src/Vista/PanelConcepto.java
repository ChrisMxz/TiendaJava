    private void cargaTabla() {
        //creando el modelo de la tabla
        DefaultTableModel modelo = conceptos.lista(txtbusca.getText());
        //tama√±o de las columnas
        int[] anchos = {50, 500};
        for (int i = 0; i < tb.getColumnCount(); i++) {
            tb.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
        }
        tb.setModel(modelo);
        tb.setDefaultEditor(Object.class, null); //La tabla no es editable  
    }
        
    private boolean FormularioOk() {
        boolean bandera = true;

        if (txtid.getText().isEmpty()) {
           bandera = false;
        }
        if (txtconcepto.getText().isEmpty()) {
            bandera = false;
        }
        return bandera;
    }
    
    private void cargarFormulario() {
        //variables a utilizar
        int id;
        String c;
        //obteneiendo datos del formulario
        id = Integer.parseInt(txtid.getText());
        c = txtconcepto.getText();

        //Insertando los datos en el objeto
        concepto.setId_concepto(id);
        concepto.setConcepto(c);
    }
